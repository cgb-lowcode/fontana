package com.bluetron.app.xxx.service.impl;

import com.bluetron.app.xxx.entity.Dept;
import com.bluetron.app.xxx.vo.DeptVO;
import com.bluetron.app.xxx.mapper.DeptMapper;
import com.bluetron.app.xxx.service.IDeptService;
import org.springblade.core.mp.base.BaseServiceImpl;
import org.springframework.stereotype.Service;
import com.baomidou.mybatisplus.core.metadata.IPage;

/**
 * 部门表 服务实现类
 *
 * @author Blade
 * @since 2022-04-21
 */
@Service
public class DeptServiceImpl extends BaseServiceImpl<DeptMapper, Dept> implements IDeptService {

	@Override
	public IPage<DeptVO> selectDeptPage(IPage<DeptVO> page, DeptVO dept) {
		return page.setRecords(baseMapper.selectDeptPage(page, dept));
	}

}
